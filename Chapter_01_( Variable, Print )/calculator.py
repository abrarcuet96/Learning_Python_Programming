# OPERATORS: 
# + addition
print(2+3)
# - subtraction
print(9-5)
# * multiplication
print(2*3)
# / float division(will get floating point number)
print(2/4)
# // integer division(will not get floating point number)
print(2//4)
# % remainder
print(3%2)
# ** exponent(2^3)
print(2**3)
print(2**0.5)

# To get upto four decimal number use round function;
print(round(2**0.5,4))

#PRECEDENCE RULE: parenthese--> exponent--> *,/,//,%(Left to Right) -->+,-(Left to Right)
print((2+3)*5/2%6)

# For exponent(Right to Left)-->(2^3^2= 2^9)
print(2**3**2)