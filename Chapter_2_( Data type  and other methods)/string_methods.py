# len() --> counts the number of characters in a string.
name="abRar"
print(len(name)) #it counts the spaces also

# Methods:
# lower() Method:
print(name.lower()) #abrar

# upper() Method:
print(name.upper()) #ABRAR

# title() Method:
print(name.title()) #Abrar(first letter will be capital)

# count() Method:
print(name.count("a")) # counts how many a's are there