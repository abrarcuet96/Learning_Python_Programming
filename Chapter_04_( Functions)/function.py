# def keyword is used to define function.

def add_two(a,b):
    return a+b

# print 2+3:
total=add_two(2,3)
print(total)

# take input and print a+b:
a=int(input("enter first number: "))
b=int(input("enter second number: "))
total=add_two(a,b)
print(total)

# It can also add characters:
first_name=(input("enter first namne: "))
second_name=(input("enter second name: "))
total=add_two(first_name,second_name)
print(total)