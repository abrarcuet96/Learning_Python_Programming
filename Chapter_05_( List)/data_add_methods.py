# Insert Method:
fruits=['mango','orange']
fruits.insert(1,'grapes') #will insert in position 1
print(fruits)

# How to join(concaenate) two list:
fruits1=['mango','orange']
fruits2=['mango','orange']
fruits=fruits1+fruits2
print(fruits)

# Extend Method:
fruits1=['mango','orange']
fruits2=['mango','orange']
fruits1.extend(fruits2)
print(fruits1)
print(fruits2)

# Append Method:
fruits1=['mango','orange']
fruits2=['mango','orange']
fruits1.append(fruits2) #list inside list
print(fruits1)
print(fruits2)