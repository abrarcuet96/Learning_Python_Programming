# More about tuple,list,string.

# Tuple using range:
num1= tuple(range(1,11))
print(num1)
print(type(num1))
# Tuple --> List:
num2=list((1,2,3,4,5,6,7))
print(num2)
print(type(num2))
# Tuple to String:
num3=str((1,2,3,4,5,6,7))
print(num3)
print(type(num3))
# List to String:
num4=str([1,2,3,4,5,6,7])
print(num4)
print(type(num4))