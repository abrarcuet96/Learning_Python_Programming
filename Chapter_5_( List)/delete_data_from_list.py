# Pop Method:
fruits=['orange','apple','pear','banana','kiwi']
fruits.pop()
print(fruits)

###
fruits=['orange','apple','pear','banana','kiwi']
fruits.pop(1) # position 1 item will be deleted
print(fruits)

# Delete operator:
fruits=['orange','apple','pear','banana','kiwi']
del fruits[1]

# Remove Method:
fruits=['orange','apple','pear','banana','kiwi']
fruits.remove('banana')
print(fruits)


